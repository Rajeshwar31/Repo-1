package basicPractice;

public class Assignment1EvenOdd {
	public static void main(String[] args) {
		
		int Number=25;
		if (Number%2==0)
		{
			System.out.println(Number +" is Even Number.");
		}
		else 
		{
			System.out.println(Number +" is Odd Number.");
		}
		
		System.out.println("End of Program.");
	}

}
