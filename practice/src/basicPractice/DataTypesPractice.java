package basicPractice;

public class DataTypesPractice {

public static void main(String[] args) {
  String Name = ("Student's Name"); //NON Primitive
  int RollNumber = 120;   //Primitive whole number excluding decimal
  Name="Rajeshwar Chaudhari"; //Non Primitive
  String EmailID="rchaudhari990@gmail.com"; //Non Primitive
  long MobileNumber = 7776983259L;  // Primitive (long) 
  char Div = 'C';  //Primitive (Char) Character type [always in single quotation]
  float Percentage = 70.82f;  // Primitive Decimal Numerical
  boolean DataisReal=true;  //boolean data type will only store true/false only variables name can differ
  System.out.println(MobileNumber);
  System.out.println(Name);
  System.out.print(RollNumber);
  System.out.println(" "+ Div);
  System.out.println(Percentage);
  System.out.println(EmailID);
  System.out.println(DataisReal);
  }

}
